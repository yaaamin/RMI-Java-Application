    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Integer itemID = Integer.parseInt(jTextField1.getText());
        Integer quantity = Integer.parseInt(jTextField2.getText());
        double price = 0.0;
        
        try {
            price = SUN.salesexecutive.calculatePrice(itemID, quantity);
        } catch (RemoteException ex) {
            Logger.getLogger(SalesExecutivePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(price != -1){
        
            itemIds.add(itemID);
            quantities.add(quantity);
            totalPrice+=price;
            refreshTableCurrentOrder();
            updateTotalPriceCounter();
            
        }
        
        else {
        
            JOptionPane.showMessageDialog(null, "Not enough stock to support purchase!");
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

