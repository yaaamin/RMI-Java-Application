    public void refreshTableCurrentOrder(){
    
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        
        model.setRowCount(0);
        
        for (int i = 0; i < itemIds.size(); i++) {
            Object[] data = {itemIds.get(i), quantities.get(i)};
            model.addRow(data);
        }
        
    }
    
    public void refreshOriginalTable(){
    
        
        ArrayList<Item> ItemList = new ArrayList<Item>();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        model.setRowCount(0);
        
        try {
            ItemList = SUN.admin.readItems();
        } catch (RemoteException ex) {
            Logger.getLogger(AdminPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        for (int i = 0; i < ItemList.size(); i++) {
            Object[] data = {ItemList.get(i).id, ItemList.get(i).name, ItemList.get(i).price, ItemList.get(i).stock};
            model.addRow(data);
        }
        
    }

